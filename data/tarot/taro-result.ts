// 카드 3장 조합에 대한 종합 해설을 생성하는 함수
// 개별 카드 포지션 해설 3개를 조합해 종합 메시지를 구성
export function getTarotSummary(
  pastCardId: number,
  presentCardId: number,
  futureCardId: number,
  category: TarotCategory
): {
  pastReading: string
  presentReading: string
  futureReading: string
  categoryMessage: string
  combinationMessage: string
} {
  const pastCard = TAROT_READINGS[pastCardId]
  const presentCard = TAROT_READINGS[presentCardId]
  const futureCard = TAROT_READINGS[futureCardId]

  // 카드 데이터가 없을 경우 기본 메시지 반환
  if (!pastCard || !presentCard || !futureCard) {
    return {
      pastReading: '과거의 에너지가 현재를 이끌고 있어요.',
      presentReading: '지금 이 순간에 집중해봐요.',
      futureReading: '앞으로 좋은 흐름이 기다리고 있어요.',
      categoryMessage: '심봉이가 당신의 이야기에 귀 기울이고 있어요.',
      combinationMessage: '세 장의 카드가 당신만의 고유한 이야기를 만들어내고 있어요.',
    }
  }

  // 카테고리별 종합 메시지 조합
  const categoryMessages: Record<TarotCategory, string> = {
    love: '연애와 관계에서 중요한 흐름이 읽혀요. 과거의 패턴을 이해하고, 현재에 솔직하며, 미래를 향해 열린 마음을 가져봐요.',
    career: '일과 진로의 흐름이 보여요. 과거에서 배운 것을 현재에 적용하고, 미래를 위한 준비를 차근차근 해나가봐요.',
    daily: '오늘 하루의 에너지를 읽어봤어요. 과거의 경험이 오늘을 안내하고, 현재의 선택이 내일을 만들어요.',
    etc: '당신의 고민에 대한 흐름이 보여요. 세 장의 카드가 각각 다른 시간에서 당신에게 말을 건네고 있어요.',
  }

  return {
    pastReading: pastCard.past,
    presentReading: presentCard.present,
    futureReading: futureCard.future,
    categoryMessage: presentCard.categoryFocus[category],
    combinationMessage: categoryMessages[category],
  }
}

